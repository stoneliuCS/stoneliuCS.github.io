◊headline{software dev reflections}

◊p{
  ◊em{
    So why the hell did you want to take this course?
  }

  I just wanted to be a better programmer. 

  ◊em{
    So how was it?
  }

  ◊em{Awful}. The workload was crazy, by the end of the semester I saw about 1/2 of my already small section dropped. It was the most intensive programming course I had ever taken in my life.

  In addition, the daily code walks made me constantly paranoid about coming to class everyday.

  ◊em{
    So what did you learn?
  }

  I'm not too sure. I learned hard skills like bash scripting, the clunkiness of our university's linux machines, some design patterns that will probably be forever engrained in my brain till as long as I live.

  ◊em{
    Anything in particular that you learned that made you a better programmer?
  }

  I can talk about building software systems from the bottom up, unit testing individual components and then integration testing components together.

  I can talk about the horrors of mutation/aliasing, and the awful code I saw up on the board everyday (Including mine).

  I can talk about the last homework assignment when me and my partner (along with about half of the class that used Java and GSON) that the library we built our entire system on could not read primitive booleans from a JSON stream properly.

  I can talk about how representing complex forms of data such as images should be in the form of a data structure that could be rendered as an image.

  Code is a message, to a future developer somewhere. If it wasn't, we would all be just writing machine code or getting stuck in the mentality of ◊em{"just getting it to work"}.

  ◊em{
    Would you do it again?
  }

  Probably.

